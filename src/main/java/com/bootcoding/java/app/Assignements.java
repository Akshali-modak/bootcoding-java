package com.bootcoding.java.app;

public class Assignements {
    public static void main(String[] args) {
        int n = 10;
        System.out.println("Even number is: ");

        for (int i = 1; i <= n; i++) {
            if (i % 2 == 0) {
                System.out.println(i + "");
            }


        }
    }
}
